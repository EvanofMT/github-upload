
CREATE TABLE IF NOT EXISTS `student_degrees_earned` (
  `id` int(8) NOT NULL AUTO_INCREMENT,
  `student_id` decimal(10,0) NOT NULL,
  `degree_type` varchar(100) DEFAULT NULL,
  `field_of_study` varchar(100) DEFAULT NULL,
  `earned_date` date DEFAULT NULL,
  `school_name` varchar(100) DEFAULT NULL,
  `school_location` varchar(100) DEFAULT NULL,
  `last_updated` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_by` varchar(255) DEFAULT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8;

CREATE TABLE `academic_programs` (
  `academic_program_id` int(8) NOT NULL AUTO_INCREMENT,
  `school_id` decimal(10,0) DEFAULT NULL,
  `syear` DECIMAL(4,0) DEFAULT NULL,
  `name` varchar(50) DEFAULT NULL,
  `sort_order` decimal(10,0) DEFAULT NULL,
  `last_updated` timestamp NOT NULL DEFAULT current_timestamp(),
  `updated_by` varchar(255) DEFAULT NULL,
  PRIMARY KEY (`academic_program_id`),
  KEY `academic_programs_ind1` (`school_id`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `academic_program_courses` (
  `program_course_id` int(8) NOT NULL AUTO_INCREMENT,  
  `syear` decimal(4,0) NOT NULL,
  `school_id` decimal(10,0) NOT NULL,
  `academic_program_id` int(8) NOT NULL,  
  `course_id` int(8) NOT NULL,
  `rollover_id` decimal(10,0) DEFAULT NULL,
  PRIMARY KEY (`program_course_id`),
  KEY `courses_ind1` (`academic_program_id`) USING BTREE,
  KEY `courses_ind2` (`course_id`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;


